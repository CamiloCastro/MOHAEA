function Y=POL(X)

    A1 = 0.5*sin(1) - 2*cos(1) + sin(2) - 1.5*cos(2);
    A2 = 1.5*sin(1) - cos(1) + 2*sin(2) - 0.5*cos(2);
    B1 = 0.5*sin(X(1,:)) - 2*cos(X(1,:)) + sin(X(2,:)) - 1.5*cos(X(2,:));
    B2 = 1.5*sin(X(1,:)) - cos(X(1,:)) + 2*sin(X(2,:)) - 0.5*cos(X(2,:));
    
    Y(1,:) = 1 + (A1-B1).^2 + (A2-B2).^2;
    Y(2,:) = (X(1,:) + 3).^2 + (X(2,:) + 1).^2;

end